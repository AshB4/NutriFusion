// Harris-Benedict Equation:

// For Men:
// Basal Metabolic Rate (BMR) = 88.362 + (13.397 x weight in kg) + (4.799 x height in cm) - (5.677 x age in years)
// Daily Calorie Needs = BMR x Activity Factor

// For Women:

// Basal Metabolic Rate (BMR) = 447.593 + (9.247 x weight in kg) + (3.098 x height in cm) - (4.330 x age in years)
// Daily Calorie Needs = BMR x Activity Factor

// Activity Factor:
// 1.2: Sedentary (little or no exercise)
// 1.375: Lightly active (light exercise/sports 1-3 days/week)
// 1.55: Moderately active (moderate exercise/sports 3-5 days/week)
// 1.725: Very active (hard exercise/sports 6-7 days/week)
// 1.9: Extremely active (very hard exercise/sports & physical job)

